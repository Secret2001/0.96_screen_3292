/*******************************************************************************
* Function Name  : math_abs
* Description    : math abs value
* Input          : s32_t m1 : vaue
*                  
* Output         : 
* Return         : 
*******************************************************************************/
u32_t math_abs(s32_t m1)
{
	if(m1>=0)
		return m1;
	else
		return (0-m1);
}
/*******************************************************************************
* Function Name  : math_max
* Description    : math max value
* Input          : s32_t m1 : value 1
*                    s32_t m2 : value 2
* Output         : 
* Return         : 
*******************************************************************************/
