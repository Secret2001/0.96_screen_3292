/*******************************************************************************
* Function Name  : logo_music_show
* Description    : layout for start music
* Input          : INT16U idx : resource id
* Output         : none                                            
* Return         : int 0;
*******************************************************************************/
int logo_music_show(INT16U idx)
{
	Audio_ARG_T music;
	int addr;	
	u32 buf_addr;
	u32 buf_size;

	addr = nv_open(idx);
    if(addr<0)
		return -1;

    music.size = nv_size(idx);
	buf_size = music.size;
	music.media.src.buff = (INT32U)hal_sysMemMalloc(music.size,64);//shareMemMalloc(music.size);
	buf_addr = music.media.src.buff;
	if(music.media.src.buff == 0 || music.size < 44)
	{
		deg_Printf("music show fail %x!\n",music.size);
		return -1;
	}
  
    nv_read(addr,(void *)music.media.src.buff,music.size);
	deg_Printf("music.size=0x%x\n",music.size);

    music.channel =1 ;
	music.type = FILE_TYPE_RAW;
	music.samplerate = 16000;
	music.media.type = MEDIA_SRC_RAM;
	music.media.src.buff += 44;	// cut wav head data
	music.size -= 44;

    audioPlaybackSetVolume(KEYSOUND_VOLUME);
	if(music.size <= 32*1024)
	{
		audioPlaybackStart(&music);
	}
	else
	{
		u32 timeout = 0x3ffffff;
		while(buf_size > 32*1024)
		{
			timeout = 0x3ffffff;
			hal_wdtClear();
			music.size = 32*1024;
			audioPlaybackStart(&music);
			while((audioPlaybackGetStatus() == MEDIA_STAT_PLAY) && timeout--);

			buf_size -= 32*1024;
			music.media.src.buff += 32*1024;
			deg_Printf("buf_size=0x%x\n",buf_size);
			hal_wdtClear();
		}
		timeout = 0x3ffffff;
		while((audioPlaybackGetStatus() == MEDIA_STAT_PLAY) && timeout--);
		music.size = buf_size;
		audioPlaybackStart(&music);
	}
	
	hal_sysMemFree((void *)buf_addr);

	return 0;
}
