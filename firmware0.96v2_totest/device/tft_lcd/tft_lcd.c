/*******************************************************************************
* Function Name  : lcdreg_debug_write
* Description    : lcd online debug
* Input          : 
* Output         : none                                            
* Return         : none
*******************************************************************************/
void lcdreg_debug_write(u8 *pbdata ,u32 trans_len, u32 param_3B){
    //debg("lcdreg_debug_write\n");
    u32 tab[64];
    memcpy(tab,pbdata,256);

    u32 i;
	if(param_3B==0)
    {
		for(i=0;(i<64) && (tab[0+i] != LCD_TAB_END) ;i+=2)
		{
			if(tab[0+i] == MCU_CMD)
                 //debg("0x%x ",tab[1+i]);
				ax32xx_lcdMcuSendCmd(tab[1+i]);
			else if(tab[0+i] == MCU_DAT)
                 //debg("1x%x ",tab[1+i]);
				ax32xx_lcdMcuSendData(tab[1+i]);
			else if(tab[0+i] == DELAY_MS)
                //debg("2x%x ",tab[1+i]);
				ax32xx_sysCpuMsDelay(tab[1+i]);
		}
	}
	else
	{
		for(i=0;(i<64) &&(tab[0+i] != LCD_TAB_END) ;i+=2)
		{
			if(tab[0+i] == MCU_CMD)
				ax32xx_lcdMcuSendCmd16(tab[1+i]);
			else if(tab[0+i] == MCU_DAT)
				ax32xx_lcdMcuSendData16(tab[1+i]);
			else if(tab[0+i] == DELAY_MS)
				ax32xx_sysCpuMsDelay(tab[1+i]);
		}
	}
}

