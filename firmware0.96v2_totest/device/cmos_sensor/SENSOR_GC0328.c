static void GC0328_rotate(u32 r)
{
	unsigned char buf[2];
	buf[0] = 0x17;		//0',180'
	buf[1] = 0x14|(r<<0);
	//	sensor_iic_enable();
	//	sensor_iic_info();	
	sensor_iic_write(buf);
	//   sensor_iic_disable();
}

static void GC0328_hvblank(u32 h,u32 v)
{
	u32 i;
	const u8 t[][2] = {
		{0x05,h >> 8},//HB[11:8]
		{0x06,h & 0xff},//HB[7:0]
		{0x07,v >> 8},//VB[11:8]
		{0x08,v & 0xff},//VB[7:0]
	};
	//	sensor_iic_enable();
	//	sensor_iic_info();		
	for(i=0;i<4;i++)
		sensor_iic_write((u8 *)&t[i][0]);
	//   sensor_iic_disable();	
}

