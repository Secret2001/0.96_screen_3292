//r:2 flip 1 mirror
static void sensor_sc1243_rotate(u32 r)
{
	//0x3221 bit[2:1]=2'b11 mirror 
	//0x3221 bit[5:4]=2'b11 flip
	//0x3210 列起始位置高八位
	//0x3211 列起始位置低八位
	//0x3212 行起始位置高八位
	//0x3213 行起始位置低八位
	u8 rotate[3] = {0x32,0x21,0x03};
	u8 r_start[3] = {0x32,0x13,0x04};
	u8 c_start[3] = {0x32,0x11,0x04};
	r_start[2] = (u8) sensor_iic_read(r_start);
	c_start[2] = (u8) sensor_iic_read(c_start);
	switch(r){
		case 0:	rotate[2] = 0x00; break;
		case 1:	rotate[2] = 0x06; break;
		case 2:	rotate[2] = 0x30; r_start[2] = r_start[2] +1;break;
		case 3:	rotate[2] = 0x36; r_start[2] = r_start[2] +1;break;
		default:	rotate[2] = 0x00;
	}
	sensor_iic_write(rotate);
	sensor_iic_write(r_start);
}

static void sensor_sc1243_hvblank(u32 h,u32 v)
{

     u32 i;
	 v = v+2;
	const u8 t[][3] = {
		{0x32,0x0c,h >> 8},
		{0x32,0x0d,h & 0xff},
		{0x32,0x0e,v >> 8},
		{0x32,0x0f,v & 0xff},
	};
	for(i=0;i<4;i++)
	    sensor_iic_write((u8 *)&t[i]);
}

