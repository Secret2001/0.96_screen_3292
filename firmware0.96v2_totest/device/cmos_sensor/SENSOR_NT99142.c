static void NT99142_rotate(u32 r)
{
	//	sensor_iic_enable();
	//	sensor_iic_info();

	if(0 == r)
	{
		u8 rot_buf[3];
		rot_buf[0] = 0x30;
		rot_buf[1] = 0x22;
		rot_buf[2] = 0x00;
		sensor_iic_write(rot_buf);
		//deg_Printf("sensor_rotate 0 \n");
	}
	else if(1 == r)
	{

		u8 rot_buf[3];
		rot_buf[0] = 0x30;
		rot_buf[1] = 0x22;
		rot_buf[2] = 0x03;
		sensor_iic_write(rot_buf);
		//deg_Printf("sensor_rotate 180 \n");
	}	
	//	 sensor_iic_diable();

}

